#include <asf.h>
void board_init(void){
	DDRB  = 0;//DIREÇÃO DOS PINO B
	PORTB = 0b00000000;//VALORES DO PINO B
	DDRC  = 0;//DIREÇÃO DOS C
	PORTC = 0b00000000;//VALORES DOS PINOS C
	DDRD  = 0b11111011;//DIREÇÃO DOS PINOS D
	PORTD = 0b00000000;//VALORES DOS PINOS D
	PCICR = 0b00000100;
	PCMSK2 |= 1<<PCINT18;
	TCCR0A=0b00000000;
	TCCR0B=0b00000101;
	TIMSK0=0b00000001;
	SREG  = 0b10000000;
}
